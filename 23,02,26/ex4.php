<?php
    $i = 1;
    $i++; // $i vaut maintenant 2 (post-incrémentation)
    // c'est équivalent à faire $i = $i + 1
    ++$i; // $i vaut maintenant 3 (pré-incrémentation)
    echo $i . "<br>";
    
    $j = 5;
    $j--; // $j vaut maintenant 4
    // c'est équivalent à faire $j = $j - 1
    --$j; // $j vaut maintenant 3
    echo $j . "<br>";
?>